import Foundation

public extension SDKEdgeInsets {
    static let ten = SDKEdgeInsets(top: 10, left: 10, bottom: 10, right: 10)
    static let twenty = SDKEdgeInsets(top: 20, left: 20, bottom: 20, right: 20)
}

public extension CGSize {
    static let ten = CGSize(width: 10, height: 10)
    static let twenty = CGSize(width: 20, height: 20)
}

public extension SDKOffset {
    static let ten = SDKOffset(horizontal: 10, vertical: 10)
    static let twenty = SDKOffset(horizontal: 20, vertical: 20)
}
